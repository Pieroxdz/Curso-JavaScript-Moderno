const carrito = [];

//Definir un producto
const producto = {
    nombre: "Monitor de 40 pulgadas",
    precio: 400
}
const producto2 = {
    nombre: "Iphone 15",
    precio: 400
}

//. Push agregar al final de un arreglo
carrito.push(producto2)
carrito.push(producto)

const producto3 = {
    nombre: "Samsung S24",
    precio: 400
}


//.Unshift agregar al inicio de un arreglo
carrito.unshift(producto3)