/* VAR ES LA FORMA ANTIGUA DE CREAR UNA VARIABLE Y YA NO SE RECOMIENDA */

/* Creamos la variable y asignamos un valor */
var producto = "Monitor de 24 pulgadas";
console.log(producto);


/* Cambiamos el valor de la variable */
producto = 'Monitor de 30 pulgadas';
console.log(producto);

/* JS es un lenguaje dinámico, no se especifica el tipo d dato */
producto = 20;
console.log(producto);

